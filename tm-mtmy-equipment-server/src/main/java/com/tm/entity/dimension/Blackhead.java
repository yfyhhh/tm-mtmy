package com.tm.entity.dimension;

public class Blackhead {
    private Integer grade;
    private Integer surfaceBlackhead;
    private Integer bottomBlackhead;

    public Integer getGrade() {
        return grade;
    }

    public void setGrade(Integer grade) {
        this.grade = grade;
    }

    public Integer getSurfaceBlackhead() {
        return surfaceBlackhead;
    }

    public void setSurfaceBlackhead(Integer surfaceBlackhead) {
        this.surfaceBlackhead = surfaceBlackhead;
    }

    public Integer getBottomBlackhead() {
        return bottomBlackhead;
    }

    public void setBottomBlackhead(Integer bottomBlackhead) {
        this.bottomBlackhead = bottomBlackhead;
    }
}
