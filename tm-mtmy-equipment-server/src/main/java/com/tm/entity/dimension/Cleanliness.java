package com.tm.entity.dimension;

public class Cleanliness {
    private Integer grade;
    private Integer grease;
    private Integer residual;

    public Integer getGrade() {
        return grade;
    }

    public void setGrade(Integer grade) {
        this.grade = grade;
    }

    public Integer getGrease() {
        return grease;
    }

    public void setGrease(Integer grease) {
        this.grease = grease;
    }

    public Integer getResidual() {
        return residual;
    }

    public void setResidual(Integer residual) {
        this.residual = residual;
    }
}
