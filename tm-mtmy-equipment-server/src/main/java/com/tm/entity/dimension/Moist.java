package com.tm.entity.dimension;

public class Moist {
    private Integer grade;
    private Integer moisture;
    private Integer oilContent;

    public Integer getGrade() {
        return grade;
    }

    public void setGrade(Integer grade) {
        this.grade = grade;
    }

    public Integer getMoisture() {
        return moisture;
    }

    public void setMoisture(Integer moisture) {
        this.moisture = moisture;
    }

    public Integer getOilContent() {
        return oilContent;
    }

    public void setOilContent(Integer oilContent) {
        this.oilContent = oilContent;
    }
}
