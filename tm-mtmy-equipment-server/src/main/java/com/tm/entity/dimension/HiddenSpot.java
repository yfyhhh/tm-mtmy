package com.tm.entity.dimension;

public class HiddenSpot {
    private Integer grade;
    private Integer surfacePigment;
    private Integer bottomPigment;

    public Integer getGrade() {
        return grade;
    }

    public void setGrade(Integer grade) {
        this.grade = grade;
    }

    public Integer getSurfacePigment() {
        return surfacePigment;
    }

    public void setSurfacePigment(Integer surfacePigment) {
        this.surfacePigment = surfacePigment;
    }

    public Integer getBottomPigment() {
        return bottomPigment;
    }

    public void setBottomPigment(Integer bottomPigment) {
        this.bottomPigment = bottomPigment;
    }
}
